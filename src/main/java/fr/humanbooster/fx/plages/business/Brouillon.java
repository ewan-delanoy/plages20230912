package fr.humanbooster.fx.plages.business;

public class Brouillon {
	private Parasol parasol1;
	private Equipement equipement1;
	private Parasol parasol2;
	private Equipement equipement2;
	private Parasol parasol3;
	private Equipement equipement3;
	
	public Brouillon() {
		super();
	}
	
	public Brouillon(Parasol parasol1, Equipement equipement1, Parasol parasol2, Equipement equipement2,
			Parasol parasol3, Equipement equipement3) {
		this();
		this.parasol1 = parasol1;
		this.equipement1 = equipement1;
		this.parasol2 = parasol2;
		this.equipement2 = equipement2;
		this.parasol3 = parasol3;
		this.equipement3 = equipement3;
	}
	
	
}
